package com.syntax.class07;

public class ForLoopExample {

	public static void main(String[] args) {
		
		for (int i = 1; i<=5; i++) {
			System.out.println("Hello");
		}
		
		System.out.println("----------------------------------");
		
		for (int a = 1; a <= 10; a++) {
			System.out.println(a);
		}
		
		System.out.println("----------------------------------");
		
		for (int i = 20; i >=10; i--) {
			System.out.println(i);
		}
		
	}
	
}
